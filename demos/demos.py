import random

class Button(object):
    def __init__(self, x, y, w, h, color, text):
        self.x1 = x
        self.y1 = y
        self.x2 = x+w
        self.y2 = y+h
        self.color = color
        self.text = text
        self.clicked = False

    def draw(self):
        rectfill(self.x1, self.y1, self.x2, self.y2, self.color)
        px8_print(self.text, self.x1+1, self.y1+1, 2)

    def update(self, x, y):
        self.clicked = False
        if x >= self.x1 and x <= self.x2:
            if y >= self.y1 and y <= self.y2:
                self.clicked = True

    def is_click(self):
        return self.clicked


