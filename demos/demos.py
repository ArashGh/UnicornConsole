class Drippy(object):
    def __init__(self):
        self.x = 64
        self.y = 64
        self.c = 8

    def init(self):
        cls()
        rectfill(0, 0, 127, 127, 1)

    def update(self):
        x = random.randint(0, 3)

        if x == 0: self.x -= 1
        if x == 1: self.x += 1
        if x == 2: self.y -= 1
        if x == 3: self.y += 1

        self.c = self.c + 0.1
        if self.c >= 16: self.c = 8

        for i in range(1, 100):
            x2 = random.randint(10, 110)
            y2 = random.randint(10, 110)

            col = pget(x2, y2)
            if col != 1: pset(x2, y2+1, col)

    def draw(self):
        pset(self.x, self.y, self.c)

